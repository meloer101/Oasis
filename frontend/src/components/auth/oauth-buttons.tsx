const PROVIDERS = [
  { id: 'google', Icon: GoogleIcon },
  { id: 'github', Icon: GitHubIcon },
  { id: 'apple', Icon: AppleIcon },
] as const

export function OAuthButtons() {
  const { t } = useLocale()

  const handleOAuth = (provider: string) => {
    window.location.href = `${API_BASE}/api/auth/oauth/${provider}`
  }

  return (
    <div className="mt-4">
      <div className="relative flex items-center gap-2 my-4">
        <div className="flex-1 border-t border-border-subtle" />
        <span className="text-xs text-text-muted">{t('auth.oauth.orContinueWith')}</span>
        <div className="flex-1 border-t border-border-subtle" />
      </div>
      <div className="flex flex-col gap-2">
        {PROVIDERS.map(({ id, Icon }) => (
          <button
            key={id}
            type="button"
            onClick={() => handleOAuth(id)}
            className="w-full border border-border-subtle rounded-lg px-4 py-3 text-sm flex items-center justify-center gap-2.5 hover:bg-surface transition-colors text-text-primary"
          >
            <Icon />
            {t(`auth.oauth.${id}`)}
          </button>
        ))}
      </div>
    </div>
  )
}
